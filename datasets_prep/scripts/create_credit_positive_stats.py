"""
Calculate statistics for positive credit instances from test set and update dataset_info pickle.

This script:
1. Loads credit_positive.csv (generated by predictions.py from the test set)
2. Calculates feature statistics (averages and distributions) for positive instances
3. Updates the dataset_info pickle to include these statistics for comparison in prompts

NOTE: credit_positive.csv must be generated by running predictions.py first!
      It contains ONLY test set instances with predicted_class == 0
"""

import pickle
import pandas as pd
import numpy as np
from pathlib import Path

# Paths
DATA_DIR = Path(__file__).parent.parent / "data" / "credit_dataset"
PICKLE_PATH = DATA_DIR / "dataset_info"
POSITIVE_CSV = DATA_DIR / "credit_positive.csv"

# Feature type definitions
CATEGORICAL_FEATURES = ['status', 'credit_history', 'purpose', 'savings', 'employment_duration', 'personal_status_sex', 'other_debtors', 'property', 'other_installment_plans', 'housing', 'job', 'telephone', 'foreign_worker']

FEATURE_MAPPINGS = {
    'status': {1: '<0 DM', 2: '0-200 DM', 3: '≥200 DM/salary', 4: 'none'},
    'credit_history': {1: 'no credits/all paid', 2: 'all paid at bank', 3: 'existing paid', 4: 'past delays', 5: 'critical/other credits'},
    'purpose': {1: 'new car', 2: 'used car', 3: 'furniture/equipment', 4: 'radio/TV', 5: 'domestic appliances', 6: 'repairs', 7: 'education', 8: 'retraining', 9: 'business', 10: 'others'},
    'savings': {1: '<100 DM', 2: '100-500 DM', 3: '500-1000 DM', 4: '≥1000 DM', 5: 'unknown/none'},
    'employment_duration': {1: 'unemployed', 2: '<1 year', 3: '1-4 years', 4: '4-7 years', 5: '≥7 years'},
    'personal_status_sex': {1: 'male divorced/separated', 2: 'female divorced/separated/married', 3: 'male single', 4: 'male married/widowed', 5: 'female single'},
    'other_debtors': {1: 'none', 2: 'co-applicant', 3: 'guarantor'},
    'property': {1: 'real estate', 2: 'building society/life insurance', 3: 'car/other', 4: 'unknown/none'},
    'other_installment_plans': {1: 'bank', 2: 'stores', 3: 'none'},
    'housing': {1: 'rent', 2: 'own', 3: 'for free'},
    'job': {1: 'unemployed/unskilled-non-resident', 2: 'unskilled-resident', 3: 'skilled/official', 4: 'management/self-employed/highly skilled'},
    'telephone': {1: 'none', 2: 'yes registered'},
    'foreign_worker': {1: 'yes', 2: 'no'},
}

def map_value(feature_name, value):
    """Map encoded values to readable names"""
    if feature_name in FEATURE_MAPPINGS:
        mapping = FEATURE_MAPPINGS[feature_name]
        if value in mapping:
            return mapping[value]
        try:
            if int(value) in mapping:
                return mapping[int(value)]
        except (ValueError, TypeError):
            pass
    return str(value)

def get_feature_distribution(feature_name, data, is_categorical):
    """Calculate distribution (value counts/percentages) for categorical features"""
    if not is_categorical:
        return None
    
    value_counts = data[feature_name].value_counts().sort_index()
    total = len(data)
    distribution_str = ""
    for value, count in value_counts.items():
        pct = (count / total) * 100
        mapped_value = map_value(feature_name, value)
        distribution_str += f"{mapped_value}: {pct:.1f}%, "
    
    return distribution_str.rstrip(", ") if distribution_str else None

def main():
    print("="*70)
    print("Calculating positive instance statistics from test set")
    print("="*70)
    
    # Check if credit_positive.csv exists
    if not POSITIVE_CSV.exists():
        print(f"\nERROR: {POSITIVE_CSV} not found!")
        print("Make sure to run predictions.py first to generate credit_positive.csv from test set")
        return
    
    # Load positive instances from test set (generated by predictions.py)
    print(f"\nLoading positive instances from {POSITIVE_CSV}...")
    df_positive = pd.read_csv(POSITIVE_CSV)
    print(f"Positive instances (predicted_class=0 from test set): {len(df_positive)}")
    
    # Load existing dataset_info pickle
    print(f"\nLoading existing pickle from {PICKLE_PATH}...")
    if not PICKLE_PATH.exists():
        print(f"ERROR: {PICKLE_PATH} not found!")
        return
    
    with open(PICKLE_PATH, 'rb') as f:
        dataset_info = pickle.load(f)
    
    feature_desc_df = dataset_info['feature_description'].copy()
    
    # Calculate statistics for positive instances for each feature
    print("\nCalculating positive instance statistics...")
    positive_stats = []
    
    for col in feature_desc_df['feature_name']:
        # Skip metadata columns
        if col in ['instance_index', 'original_test_index', 'predicted_class', 'prediction_score', 'target_credit']:
            continue
        
        # Skip if column not in data
        if col not in df_positive.columns:
            print(f"  WARNING: {col} not found in positive CSV")
            positive_stats.append({
                'feature_name': col,
                'feature_average_positive': None,
                'feature_distribution_positive': None
            })
            continue
            
        is_cat = col in CATEGORICAL_FEATURES
        
        # Calculate average (only for non-categorical)
        avg_positive = df_positive[col].mean() if not is_cat else None
        
        # Calculate distribution (only for categorical)
        dist_positive = get_feature_distribution(col, df_positive, is_cat) if is_cat else None
        
        positive_stats.append({
            'feature_name': col,
            'feature_average_positive': avg_positive,
            'feature_distribution_positive': dist_positive
        })
        
        if is_cat and dist_positive:
            print(f"  {col}: {dist_positive}")
        elif avg_positive is not None:
            print(f"  {col}: avg = {avg_positive:.3f}")
    
    # Merge positive stats back into feature_desc_df
    positive_stats_df = pd.DataFrame(positive_stats)
    feature_desc_updated = feature_desc_df.merge(positive_stats_df, on='feature_name', how='left')
    
    # Update dataset_info
    dataset_info['feature_description'] = feature_desc_updated
    
    print(f"\nUpdating pickle with positive instance statistics...")
    with open(PICKLE_PATH, 'wb') as f:
        pickle.dump(dataset_info, f)
    print(f"✓ Updated {PICKLE_PATH}")
    
    print("\n" + "="*70)
    print("✓ Complete! Ready to use positive instance statistics in prompts")
    print("="*70)

if __name__ == "__main__":
    main()
